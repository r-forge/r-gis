setMethod('c', signature(x='SpatRaster'), 
	function(x, ...) {
		for (i in list(...)) {
			if (class(i) == 'SpatRaster') {
				x@ptr <- x@ptr$addSources(i@ptr)
			}
		}
		x@ptr$setNames(x@ptr$names)
		.messages(x, "c")		
		return(x)
	}
)

